let x = 1
let y = 2

console.log("ADD", add(x, y))
console.log("SUBTRACT", subtract(x, y))
console.log("MULTIPLY", multiply(x, y))
console.log("DIVIDE", divide(x,y))

function add(x,y){
  return x+y
}
function subtract(x,y){
  return x-y
}
function multiply(x,y){
  return x*y
}
function divide(x,y){
  return x/y
}

